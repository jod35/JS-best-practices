let myArray=[1,2,3,4,5,6,7,8,9,0];

//we can use pop() to remove from the end of the array 
console.log(myArray.pop()); //it also returns what it has popped

console.log(myArray);
// [ 1, 2, 3, 4, 5, 6, 7, 8, 9 ]

//we use shift() to remove from the start of array of the array
console.log(myArray.shift()); // shows us 1

console.log(myArray);

//[ 2, 3, 4, 5, 6, 7, 8, 9 ]

